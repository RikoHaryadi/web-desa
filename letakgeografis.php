        function getLatLong(x, y) {
            var hasil;
            if (x == "Rectangle" || x == "Line" || x == "Poly") {
                hasil = JSON.stringify(y._latlngs);
            } else if (x == "multi") {
                hasil = JSON.stringify(y);
            } else {
                hasil = JSON.stringify(y._latlng);
            }

            hasil = hasil
                .replace(/\}/g, "]")
                .replace(/(\{)/g, "[")
                .replace(/(\"lat\"\:|\"lng\"\:)/g, "")
                .replace(/(\"alt\"\:)/g, "")
                .replace(/(\"ele\"\:)/g, "");

            return hasil;
        }

